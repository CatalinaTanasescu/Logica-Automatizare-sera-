/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_I2C1_Init();
  MX_USART2_UART_Init();
  MX_UART4_Init();
  MX_ADC1_Init();
  /* USER CODE BEGIN 2 */
  // Initialize BMP180 and read calibration data
  BMP180_ReadCalibrationData(&calibrationData);
  SendCalibrationData(&calibrationData);  // Send calibration data over UART
  cbuff_new(&temperature_buffer, MAX_NO_TEMP);
  Fan_Init();  // Initialize fan control GPIOs
  SoilMoisture_Init();

  /* USER CODE END 2 */

  /* Initialize leds */
  BSP_LED_Init(LED_GREEN);
  BSP_LED_Init(LED_YELLOW);
  BSP_LED_Init(LED_RED);

  /* Initialize USER push-button, will be used to trigger an interrupt each time it's pressed.*/
  BSP_PB_Init(BUTTON_USER, BUTTON_MODE_EXTI);

  /* Initialize COM1 port (115200, 8 bits (7-bit data + 1 stop bit), no parity */
  BspCOMInit.BaudRate   = 115200;
  BspCOMInit.WordLength = COM_WORDLENGTH_8B;
  BspCOMInit.StopBits   = COM_STOPBITS_1;
  BspCOMInit.Parity     = COM_PARITY_NONE;
  BspCOMInit.HwFlowCtl  = COM_HWCONTROL_NONE;
  if (BSP_COM_Init(COM1, &BspCOMInit) != BSP_ERROR_NONE)
  {
    Error_Handler();
  }

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {

    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
	  // Step 1: Start temperature measurement and calculate temperature
	      BMP180_WriteReg(0xF4, 0x2E);
	      HAL_Delay(5);  // Wait for temperature conversion (4.5ms typical)

	      // Read and calculate temperature
	      int16_t raw_temperature = BMP180_ReadTemperature();
	      int32_t x1 = ((raw_temperature - calibrationData.ac6) * calibrationData.ac5) >> 15;
	      int32_t x2 = (calibrationData.mc << 11) / (x1 + calibrationData.md);
	      int32_t b5 = x1 + x2;
	      float temperature = (float)((b5 + 8) >> 4) / 10.0;  // Temperature in °C

	      // Step 2: Start pressure measurement with OSS=1 (standard resolution)
	      uint8_t oss = 1;  // Set oversampling setting (0 to 3)
	      int32_t uncompensatedPressure = BMP180_ReadUncompensatedPressure(oss);
	      int32_t pressure = BMP180_CalculatePressure(uncompensatedPressure, b5, oss, &calibrationData);

	      // Step 3: Transmit temperature and pressure data via UART
	      char uartBuffer[100];
	      int tempInt = (int)temperature;
	      int tempFrac = (int)((temperature - tempInt) * 10);  // Single decimal precision
	      snprintf(uartBuffer, sizeof(uartBuffer), "Temperature: %d.%d C\r\n", tempInt, tempFrac);
	      HAL_UART_Transmit(&huart2, (uint8_t *)uartBuffer, strlen(uartBuffer), HAL_MAX_DELAY);

	      snprintf(uartBuffer, sizeof(uartBuffer), "Pressure: %ld Pa\r\n", pressure);
	      HAL_UART_Transmit(&huart2, (uint8_t *)uartBuffer, strlen(uartBuffer), HAL_MAX_DELAY);

	      // Fan control logic
	      if (temperature > 25.0) {
	          // Display message and turn on the fan if temperature exceeds 25°C
	          snprintf(uartBuffer, sizeof(uartBuffer), "Temperature greater than 25 degrees Celsius\r\n");
	          HAL_UART_Transmit(&huart2, (uint8_t *)uartBuffer, strlen(uartBuffer), HAL_MAX_DELAY);

	          Fan_Control(1);  // Assume 1 turns the fan ON
	          snprintf(uartBuffer, sizeof(uartBuffer), "Fan ON\r\n");
	          HAL_UART_Transmit(&huart2, (uint8_t *)uartBuffer, strlen(uartBuffer), HAL_MAX_DELAY);
	      } else {
	          // Turn off the fan if temperature is below or equal to 25°C
	          Fan_Control(0);  // Assume 0 turns the fan OFF
	          snprintf(uartBuffer, sizeof(uartBuffer), "Fan OFF\r\n");
	          HAL_UART_Transmit(&huart2, (uint8_t *)uartBuffer, strlen(uartBuffer), HAL_MAX_DELAY);
	      }
	      SoilMoisture_DisplayData();
	      // Delay before next measurement
	      HAL_Delay(1000);

  }
  /* USER CODE END 3 */
}

